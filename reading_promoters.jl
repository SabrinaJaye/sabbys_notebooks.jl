Ok, I'm going to write a little function. It's going to take a filename as input.
It will read each line of the file as a single string, it will split those strings on the tab character, as these files are tab-delimited. And for each split line, we'll turn the second value - the text of the DNA sequence, into an actual BioSequences type so we can use all the BioJulia features. 
"""

# ╔═╡ 26e5a32d-cd4e-4692-84ce-b6bf30d545c2
function read_promoter_seq_file(path)
	text_lines = readlines(path)
	map(text_lines) do line
		split_text = split(line, '\t')
		promoter_id = split_text[1]
		promoter_seq = LongDNA{4}(split_text[2])
		return promoter_id => promoter_seq
	end
end

